/**
 * Subtracts one number from another.
 *
 * @param x - first number
 * @param y - second number
 * @returns the second number subtracted from the first
 *
 * @example two positive numbers
 * ```js
 * subtract(3, 1);  // => 2
 * ```
 * @example subtracting a negative number
 * ```js
 * add(2, -1); // => 1
 * ```
 */
export const subtract = (x: number, y: number): number => {
  return x - y;
};
